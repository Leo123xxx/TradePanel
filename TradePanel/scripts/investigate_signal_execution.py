def check_paper_engine():
    """Check PaperEngine code for execution paths."""
    print("\n" + "="*80)
    print("STEP 6: PAPER ENGINE EXECUTION PATHS")
    print("="*80)

    paper_engine_path = PROJECT_ROOT / "forward_test" / "paper_engine.py"

    if paper_engine_path.exists():
        with open(paper_engine_path) as f:
            content = f.read()

        # Check for key methods
        methods = [
            ("run_detect", "Signal detection (1-min scan)"),
            ("run_execute", "Trade execution (5-min scan)"),
            ("_process_symbol", "Process single symbol for signals"),
            ("order_manager.open_position", "Submit order to MT5"),
        ]

        print("Key execution methods found:\n")
        for method, description in methods:
            found = "✓" if method in content else "✗"
            print(f"  {found} {method:35} - {description}")

        # Check mode handling
        if "if mode == 'detect'" in content:
            print("\n✓ Mode-based execution logic found")
            print("  - detect mode: Scans for signals only")
            print("  - execute mode: Processes and executes signals")

        # Check risk manager check
        if "risk_manager.check_all" in content:
            print("\n✓ Risk manager integration found")
            print("  - Checks run before order submission")

        if "order_manager.open_position" in content:
            print("\n✓ OrderManager integration found")
            print("  - Positions submitted via order_manager.open_position()")

