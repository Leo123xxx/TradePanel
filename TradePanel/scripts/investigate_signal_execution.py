def check_risk_manager():
    """Check what risk checks might block signals."""
    print("\n" + "="*80)
    print("STEP 3: RISK MANAGER CHECKS")
    print("="*80)

    config_path = PROJECT_ROOT / "config" / "config.yaml"
    with open(config_path) as f:
        config = yaml.safe_load(f)

    risk_cfg = config.get("risk_management", {})

    checks = [
        ("Bot Paused", "Check bot_health for MANUAL_PAUSE / CIRCUIT_BREAKER"),
        ("Blocked Pairs", f"blocked_pairs: {risk_cfg.get('blocked_pairs', [])}"),
        ("News Blackout", "Check bot_health for NEWS_BLACKOUT"),
        ("Strategy Active", "Check if strategy is enabled in strategies.yaml"),
        ("Market Regime", f"use_macro_regime_filter: {risk_cfg.get('use_macro_regime_filter', False)}"),
        ("Max Concurrent", f"max_concurrent_positions: {risk_cfg.get('max_concurrent_positions', 5)}"),
        ("Max Lot Size", f"max_lot_size: {risk_cfg.get('max_lot_size', 1.0)}"),
        ("Margin Check", "Requires sufficient free margin in MT5"),
        ("Max Spread", f"max_spread_pips: {risk_cfg.get('max_spread_pips', 5)}"),
        ("Trading Hours", "Must be within configured hours/days"),
        ("HTF Trend", "D1 EMA 200 alignment (if enabled)"),
        ("Macro Bias", "Global bias alignment (if enabled)"),
    ]

    print("Risk Manager runs these checks on every signal:\n")
    for i, (check_name, detail) in enumerate(checks, 1):
        print(f"{i:2}. {check_name:20} - {detail}")

    print("\n⚠️  ANY FAILED CHECK BLOCKS SIGNAL EXECUTION")

