import sys
import yaml
import os
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

def check_config():
    """Check if system is in paper mode."""
    print("\n" + "="*80)
    print("STEP 1: CHECK TRADING MODE")
    print("="*80)

    config_path = PROJECT_ROOT / "config" / "config.yaml"
    with open(config_path) as f:
        config = yaml.safe_load(f)

    system_mode = config.get("system", {}).get("mode", "unknown")
    print(f"✓ System mode: {system_mode}")

    if system_mode == "paper":
        print("  ⚠️  PAPER MODE ENABLED - Trades will NOT execute on live MT5")
        print("     To enable live trading, set config.system.mode = 'live'")
    elif system_mode == "live":
        print("  ✅ LIVE MODE - Trades WILL execute on real account")

    # Check trading hours
    trading_start = config.get("trading", {}).get("session_start", "00:30")
    trading_end = config.get("trading", {}).get("session_end", "23:59")
    trading_days = config.get("trading", {}).get("days", [0,1,2,3,4])
    print(f"✓ Trading hours: {trading_start} to {trading_end}")
    print(f"✓ Trading days: {trading_days} (0=Mon, 4=Fri)")

    # Check signal execution interval
    signal_exec_interval = config.get("scheduler", {}).get("signal_outcome_check_interval", 900)
    print(f"✓ Signal execution check interval: {signal_exec_interval} seconds ({signal_exec_interval//60} min)")

    return config

